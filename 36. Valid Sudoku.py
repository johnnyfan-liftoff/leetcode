from curses.ascii import isdigit


def isValidSudoku(board):
  # check row
  
  for i in range(len(board)):
    r = set()
    for j in range(len(board[0])):
      if board[i][j].isdigit():
        if board[i][j] not in r:
          r.add(board[i][j])
        else:
          return False

  # check col
  for i in range(len(board)):
    r = set()
    for j in range(len(board[0])):
      if board[j][i].isdigit():
        if board[j][i] not in r:
          r.add(board[j][i])
        else:
          return False

  # check diag
  for k in range(0, len(board)*3, 3):
    start, end = k // 9, k % 9
    r = set()
    for i in range(3):
      for j in range(3):
        if board[start * 3 + i][end + j].isdigit():
          if board[start * 3 + i][end + j] not in r:
            r.add(board[start  * 3 + i][end + j])
          else:
            return False
  return True


